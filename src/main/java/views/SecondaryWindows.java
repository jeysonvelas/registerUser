    private void buttonOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOkActionPerformed

        String nombre, apellido, email, Telefono;

        nombre = campoText1.getText();
        apellido = campoText2.getText();
        email = campoText3.getText();
        Telefono = campoText4.getText();

        if (nombre.isEmpty() || apellido.isEmpty() || email.isEmpty() || Telefono.isEmpty()) {

            JOptionPane.showMessageDialog(null, "faltan datos no pueden quedar ninguna casilla vacia");
            setVisible(true);

        } else {

            Persona persona = new Persona(nombre, apellido, email, Telefono);

            PersonaDAO personaDAO = new PersonaDAO();

            personaDAO.insertarDatos(persona);

            campoText1.setText("");
            campoText2.setText("");
            campoText3.setText("");
            campoText4.setText("");

            JOptionPane.showMessageDialog(null, "La persona se registo con exito");

            setVisible(false);
        }


    }//GEN-LAST:event_buttonOkActionPerformed

